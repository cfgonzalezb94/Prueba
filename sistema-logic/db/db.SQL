
CREATE TABLE CARGO ( 
	ID integer NOT NULL,
	NOMBRE varchar(50),
	DESCRIPCION varchar(50)
);

CREATE TABLE CATEGORIA ( 
	ID integer NOT NULL,
	NOMBRE varchar(50),
	DESCRIPCION varchar(50)
);

CREATE TABLE COMENTARIOS ( 
	ID integer NOT NULL,
	TITULO varchar(50),
	DESCRIPCION text,
	ID_MARCA integer
);

CREATE TABLE MARCA ( 
	ID integer NOT NULL,
	NOMBRE varchar(50),
	DESCRIPCION varchar(50),
	RANKING double precision,
	VALOR_DECIMAL integer,
	RUTA_LOGO varchar(50),
	RUTA_PORTADA varchar(50),
	ID_CATEGORIA integer,
	ID_USUARIO integer,
	ESTADO varchar(1)
);

CREATE TABLE SUB_MARCA ( 
	ID integer NOT NULL,
	NOMBRE varchar(50),
	DESCRIPCION varchar(50),
	RANKING double precision,
	VALOR_DECIMAL integer,
	RUTA_LOGO varchar(50),
	RUTA_PORTADA varchar(50),
	ID_CATEGORIA integer,
	ID_MARCA integer
);

CREATE TABLE TIPOS_SUSCRIPCION ( 
	ID integer NOT NULL,
	NOMBRE varchar(50),
	DESCRIPCION varchar(50)
);

CREATE TABLE USUARIO ( 
	ID integer NOT NULL,
	NOMBRE varchar(50),
	ID_CARGO integer,
	NOMBRE_DELEGADO varchar(50),
	EMAIL_DELEGADO varchar(50),
	USUARIO varchar(20),
	CONTRASENA varchar(20),
	CODIGO_REGISTRO varchar(6),
	RUTA_LOGO varchar(100),
	ID_TIPO_SUSCRIPCION integer,
	FECHA_REGISTRO timestamp
);


ALTER TABLE CARGO ADD CONSTRAINT PK_CARGO 
	PRIMARY KEY (ID);


